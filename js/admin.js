/* ================= Tasks & Schema builder ================= */
function currentTasks() { return ADMIN_LIVE ? L_TASKS : ADMIN_TASKS; }

function renderTaskList() {
  var list = document.getElementById('adminTaskList');
  list.innerHTML = '';
  var tasks = currentTasks();
  for (var i = 0; i < tasks.length; i++) {
    (function (task, idx) {
      var el = document.createElement('div');
      el.className = 't-item' + (task.enabled ? '' : ' off') + (adminState.selectedTask === idx ? ' active' : '');
      el.innerHTML = '<span>' + task.icon + '</span><span>' + esc(task.name) + '</span><span class="t-role">' + (ROLES[task.role] || task.role) + '</span>';
      el.onclick = function () { selectTask(idx); };
      list.appendChild(el);
    })(tasks[i], i);
  }
}

function selectTask(idx) {
  adminState.selectedTask = idx;
  renderTaskList();
  var task = currentTasks()[idx];
  document.getElementById('builderEmpty').style.display = 'none';
  document.getElementById('builderForm').style.display = 'block';
  document.getElementById('bName').value = task.name;
  document.getElementById('bIcon').value = task.icon;
  document.getElementById('bRole').value = task.role;
  document.getElementById('bInstructions').value = task.instructions;
  document.getElementById('disableTaskBtn').textContent = task.enabled ? 'تعطيل المهمة' : 'تفعيل المهمة';
  renderFields(task);
  renderPreview(task);
}

function renderFields(task) {
  var wrap = document.getElementById('fieldsList');
  wrap.innerHTML = '';
  for (var i = 0; i < task.fields.length; i++) {
    (function (f, idx) {
      var row = document.createElement('div');
      row.className = 'f-row';

      var inp = document.createElement('input');
      inp.type = 'text';
      inp.value = f.label;
      inp.addEventListener('input', function () { f.label = inp.value; renderPreview(task); });

      var sel = document.createElement('select');
      var types = [['text', 'نص قصير'], ['textarea', 'نص طويل'], ['select', 'قائمة اختيار']];
      for (var t = 0; t < types.length; t++) {
        var op = document.createElement('option');
        op.value = types[t][0];
        op.textContent = types[t][1];
        sel.appendChild(op);
      }
      sel.value = f.type;
      sel.addEventListener('change', function () { f.type = sel.value; renderPreview(task); });

      var del = document.createElement('button');
      del.className = 'f-del';
      del.textContent = '✕';
      del.title = 'حذف الحقل';
      del.onclick = function () {
        task.fields.splice(idx, 1);
        renderFields(task);
        renderPreview(task);
      };

      row.appendChild(inp);
      row.appendChild(sel);
      row.appendChild(del);
      wrap.appendChild(row);
    })(task.fields[i], i);
  }
}

function addField() {
  if (adminState.selectedTask === null) return;
  var task = currentTasks()[adminState.selectedTask];
  task.fields.push({ label: 'حقل جديد', type: 'text' });
  renderFields(task);
  renderPreview(task);
}

function saveTask() {
  if (adminState.selectedTask === null) return;
  var task = currentTasks()[adminState.selectedTask];
  task.name = document.getElementById('bName').value.trim() || task.name;
  task.icon = document.getElementById('bIcon').value.trim() || task.icon;
  task.role = document.getElementById('bRole').value;
  task.instructions = document.getElementById('bInstructions').value;

  var btn = document.getElementById('saveTaskBtn');

  if (ADMIN_LIVE) {
    if (!task.fields.length) { alert('أضف حقلاً واحداً على الأقل'); return; }
    btn.disabled = true; btn.textContent = 'جارٍ الحفظ...';
    var payload = editorToPayload(task);
    fetch('/api/admin-tasks', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        action: 'save', id: task.id, role: task.role,
        enabled: task.enabled, instructions: task.instructions, payload: payload
      })
    })
      .then(function (r) { return r.json(); })
      .then(function (d) {
        btn.disabled = false;
        if (d.ok) {
          task.payload = payload;
          btn.textContent = '✔ حُفظ في قاعدة البيانات';
          renderTaskList(); renderPreview(task); renderStats();
        } else {
          btn.textContent = 'حفظ';
          alert('خطأ في الحفظ: ' + d.error);
        }
        setTimeout(function () { btn.textContent = 'حفظ'; }, 1800);
      })
      .catch(function () { btn.disabled = false; btn.textContent = 'حفظ'; alert('تعذر الاتصال بالخادم'); });
    return;
  }

  renderTaskList();
  renderPreview(task);
  btn.textContent = '✔ حُفظ (في الذاكرة فقط)';
  setTimeout(function () { btn.textContent = 'حفظ (تجريبي)'; }, 1600);
}

function toggleTaskEnabled() {
  if (adminState.selectedTask === null) return;
  var task = currentTasks()[adminState.selectedTask];
  task.enabled = !task.enabled;
  document.getElementById('disableTaskBtn').textContent = task.enabled ? 'تعطيل المهمة' : 'تفعيل المهمة';

  if (ADMIN_LIVE) {
    fetch('/api/admin-tasks', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ action: 'toggle', id: task.id, enabled: task.enabled })
    });
  }
  renderTaskList();
  renderStats();
}

