function dbTaskToEditor(t) {
  var p = t.payload;
  return {
    id: t.id,
    icon: p.icon || '🆕',
    name: (p.ar && p.ar[0]) || t.id,
    role: t.role,
    enabled: t.enabled,
    instructions: t.instructions || '',
    fields: (p.fields || []).map(function (f) { return { label: f.ar, type: f.type }; }),
    payload: p
  };
}

function editorToPayload(task) {
  var p = task.payload || {
    icon: '🆕',
    ar: [task.name, 'مهمة جديدة'],
    en: [task.name, 'New task'],
    steps: task.fields.length,
    fields: [],
    result: {
      ar: 'تم استلام البيانات — المخرَج يولده المساعد الذكي وفق تعليمات الإدارة.',
      en: 'Data received — the output is generated by the assistant per management instructions.'
    }
  };
  p.icon = task.icon;
  p.ar = p.ar || [task.name, '']; p.ar[0] = task.name;
  p.en = p.en || [task.name, ''];
  p.steps = task.fields.length;

  var oldFields = p.fields || [];
  p.fields = task.fields.map(function (f, i) {
    var old = oldFields[i];
    if (old && old.type === f.type) {
      old.ar = f.label;
      if (!old.en) old.en = f.label;
      return old;
    }
    var nf = { k: 'f' + (i + 1) + '_' + Math.random().toString(36).slice(2, 6), ar: f.label, en: f.label, type: f.type };
    if (f.type === 'select') {
      nf.opts = (old && old.opts) || { ar: ['اختر...', 'خيار 1', 'خيار 2'], en: ['Choose...', 'Option 1', 'Option 2'] };
    }
    return nf;
  });
  return p;
}

