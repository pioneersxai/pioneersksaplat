/* ================= Employees ================= */
function renderEmployees() {
  var body = document.getElementById('empTableBody');
  body.innerHTML = '';
  var emps = ADMIN_LIVE ? L_EMPS : EMPLOYEES;

  for (var i = 0; i < emps.length; i++) {
    (function (emp) {
      var tr = document.createElement('tr');

      var tdName = document.createElement('td');
      tdName.textContent = emp.name + (ADMIN_LIVE ? ' (' + emp.username + ')' : '');

      var tdRole = document.createElement('td');
      var sel = document.createElement('select');
      for (var r in ROLES) {
        if (ROLES.hasOwnProperty(r)) {
          var op = document.createElement('option');
          op.value = r;
          op.textContent = ROLES[r];
          sel.appendChild(op);
        }
      }
      var opA = document.createElement('option');
      opA.value = 'admin'; opA.textContent = '⭐ أدمن';
      sel.appendChild(opA);
      sel.value = emp.role;
      sel.addEventListener('change', function () {
        emp.role = sel.value;
        if (ADMIN_LIVE) empUpdate({ id: emp.id, role: sel.value });
      });
      tdRole.appendChild(sel);

      var tdState = document.createElement('td');
      tdState.innerHTML = emp.active
        ? '<span class="pill on">مفعّل</span>'
        : '<span class="pill off">موقوف</span>';

      var tdAct = document.createElement('td');
      var btn = document.createElement('button');
      btn.className = 'chip-btn';
      btn.textContent = emp.active ? 'إيقاف' : 'تفعيل';
      btn.onclick = function () {
        emp.active = !emp.active;
        if (ADMIN_LIVE) empUpdate({ id: emp.id, active: emp.active ? 1 : 0 }, renderEmployees);
        else renderEmployees();
      };
      tdAct.appendChild(btn);

      if (ADMIN_LIVE) {
        var pw = document.createElement('button');
        pw.className = 'chip-btn';
        pw.textContent = '🔑';
        pw.title = 'تغيير كلمة المرور';
        pw.onclick = function () {
          var np = prompt('كلمة المرور الجديدة لـ ' + emp.name + ' (8 أحرف على الأقل):');
          if (np && np.length >= 8) empUpdate({ id: emp.id, password: np }, function () { alert('تم تغيير كلمة المرور ✔'); });
          else if (np) alert('قصيرة جداً — 8 أحرف على الأقل');
        };
        tdAct.appendChild(document.createTextNode(' '));
        tdAct.appendChild(pw);
      }

      tr.appendChild(tdName);
      tr.appendChild(tdRole);
      tr.appendChild(tdState);
      tr.appendChild(tdAct);
      body.appendChild(tr);
    })(emps[i]);
  }
  renderStats();
}

function empUpdate(fields, cb) {
  fields.action = 'update';
  fetch('/api/employees', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(fields)
  })
    .then(function (r) { return r.json(); })
    .then(function (d) {
      if (!d.ok) { alert('خطأ: ' + d.error); loadEmployees(); }
      else if (cb) cb();
    })
    .catch(function () { alert('تعذر الاتصال بالخادم'); });
}

